package samplepackage;

public class SwitchCondition {

	public static void main(String[] args) {
		char grade= 'A';
		
		switch (grade){
		    
		   case 'A':
		       System.out.println("Axcellent");
		       break;
		       
		   case 'B':
		       System.out.println("Good");
		       break;
		       
		       
		   case 'C':
		       System.out.println("Better");
		       break;
		       
		   default:
		       System.out.println("Invalid");
		    
		}

	}

}
