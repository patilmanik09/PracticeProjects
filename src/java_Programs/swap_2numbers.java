package java_Programs;

public class swap_2numbers {

	public static void main(String[] args) {
		
		int num1= 100;
		int num2= 50;
		
		num1 = num1+num2;
		num2= num1-num2;//num2 = 100;
		num1= num1-num2;//num1=50;
		
		System.out.println("num1 is " +num1);
		System.out.println("num2 is "+num2);

	}

}
